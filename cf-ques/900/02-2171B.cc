#include <bits/stdc++.h>
using namespace std;
#define ll long long

void solve() {
    ll n; cin>>n;
    vector<ll> arr(n);
    for (int i=0; i<n; i++) cin>>arr[i];
}

int main() {
    ios_base::sync_with_stdio(false); cin.tie(nullptr);
    int t; cin>>t;
    while (t--) solve();
    return 0;
}

